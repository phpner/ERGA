        $(window).on('resize orientationchange', function() {
            fResize();
        });

        function fResize() {
            if ($(window).width() <= 768) {
                $('.nav_slider').slick('unslick');
                $('.nav_slider').slick({
                    slidesToShow: 3,
                    asNavFor: '.slider_photo',
                    vertical: false,
                    focusOnSelect: true,
                    autoplay: true,
                   centerMode: true,
                });

                if ($(window).width() <= 500){
                    $('.nav_slider').slick('unslick');
                    $('.nav_slider').slick({
                        slidesToShow: 2,
                        asNavFor: '.slider_photo',
                        vertical: false,
                        focusOnSelect: true,
                        autoplay: true,
                        centerMode: true,
                    });
                }

                console.log($(window).width());
            }else {

                $('.nav_slider').slick('unslick');
                $('.nav_slider').slick({
                    slidesToShow: 3,
                    asNavFor: '.slider_photo',
                    vertical: true,
                    focusOnSelect: true,
                    autoplay: true,
                    centerMode: true,
                    daptiveHeight: false,

                });

            }


        }

        //==========Tabs========//

        $(".tab_panel").on('click',function(e){

            e.preventDefault();

            var WhoClick = $(e.target).attr("href");

            var tabPanel = $(this).find(".tab-content").find(".tab-pane ");

            var menu = $(this).find(".nav-tabs").find(".nav-link ");


            menu.each(function () {
                if (typeof WhoClick === "undefined") return;
                $(this).removeClass("active");
                console.log(WhoClick)
                var href = $(this).attr('href');

                if (href === WhoClick){
                    $(this).addClass('active');

                }
            });

            tabPanel.each(function (i) {
                var to = $(this).attr("id");
                if(typeof  WhoClick === "undefined") return;
                var sliceWho = WhoClick.slice(1);
                //$(this).removeClass("show");
                $(this).fadeOut(0);
                if (to === sliceWho){
                    $(this).fadeIn(200);
                   setTimeout(function () {
                       $(window).trigger('resize');
                      // $(".nav_slider .slick-next").trigger('click');
                       $(".slider_photo .slick-next").trigger('click');
                       console.log('clock!');
                   },250);
                    // $(this).addClass("show");
                }
            })

        });

    });
